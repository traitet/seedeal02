import 'package:scoped_model/scoped_model.dart';

//==========================================================================
// MAIN MODEL
//==========================================================================
class MainModel extends Model {
//==========================================================================
// DECLARE VARIABLE
//==========================================================================
  String _name = "";
  int _count = 0;
  String _username = '';

//==========================================================================
// GET USERNAME
//==========================================================================
String get username {
  return _username;
}
//==========================================================================
// SET USERNAME
//==========================================================================
void updateUserName(String username){
  _username = username;
}
//==========================================================================
// GET NAME
//==========================================================================
  String get name {
    return _name;
  }
//==========================================================================
// GET COUNT
//==========================================================================
  int get count {
    return _count;
  }
//==========================================================================
// SET NAME
//==========================================================================
  void updateName(String name) {
    _name = name;
  }
//==========================================================================
// FUNCTION: INCEASE COUNT
//==========================================================================
  void incrementCount() {
    _count += 1;
    notifyListeners();
  }
}                                                                                                                                                     